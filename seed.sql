## 프롬프트 개선 팁

1. **구체적일수록 좋습니다** - 숫자, 이름, 장소를 넣으세요
2. **예시를 보여주세요** - "이런 느낌으로 써줘" + 예시 1개
3. **형식을 지정하세요** - 표, 번호 목록, 단락 등
4. **반복 수정하세요** - 한 번에 완벽하지 않아도 됩니다. "더 짧게", "더 캐주얼하게" 요청

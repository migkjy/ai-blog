## 프롬프트 기본 공식

```
[역할] 당신은 ___입니다.
[맥락] 상황은 ___입니다.
[요청] ___을 해주세요.
[형식] ___형태로 결과를 주세요.
```

### 나쁜 예시 vs 좋은 예시

**나쁜 예시:**
> "마케팅 방법 알려줘"

**좋은 예시:**
> "당신은 10년 경력의 소셜미디어 마케팅 전문가입니다. 서울 마포구에서 수제 버거집을 운영하는 사장님에게, 인스타그램 팔로워를 3개월 안에 1000명에서 5000명으로 늘리는 구체적인 전략을 제안해주세요. 주 예산은 30만원이고, 사장님이 직접 실행할 수 있는 수준이어야 합니다. 주차별 액션 플랜 표로 정리해주세요."

결과의 차이는 **하늘과 땅**입니다.


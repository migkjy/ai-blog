## 실전 프롬프트 템플릿 5가지

### 1. 고객 페르소나 만들기

```
당신은 마케팅 리서치 전문가입니다.

[업종]을 운영하고 있습니다.
주요 제품/서비스는 [___]이고, 가격대는 [___]입니다.
매장 위치는 [___]입니다.

우리 가게의 이상적인 고객 페르소나 3가지를 만들어주세요.
각 페르소나에 이름, 나이, 직업, 라이프스타일, 우리 가게를 찾는 이유,
선호하는 SNS 채널, 구매 패턴을 포함해주세요.
```

### 2. 경쟁사 분석

```
당신은 비즈니스 애널리스트입니다.

[업종] 시장에서 아래 경쟁사들을 분석해주세요:
- 경쟁사1: [URL 또는 설명]
- 경쟁사2: [URL 또는 설명]
- 경쟁사3: [URL 또는 설명]

각 경쟁사의 강점, 약점, 가격 전략, 마케팅 방식을 비교 분석하고,
우리가 차별화할 수 있는 포인트를 제안해주세요.
표 형태로 정리해주세요.
```

### 3. 주간 콘텐츠 캘린더

```
당신은 콘텐츠 마케터입니다.

[업종] 비즈니스의 인스타그램을 위한
다음 주(월~금) 콘텐츠 캘린더를 만들어주세요.

브랜드 톤: [친근한/전문적/유머러스 등]
타겟 고객: [설명]
최근 반응 좋았던 콘텐츠: [설명]

각 포스트에 주제, 캡션, 해시태그, 최적 발행 시간을 포함해주세요.
```

### 4. 이메일 시퀀스

```
당신은 이메일 마케팅 전문가입니다.

새로 가입한 고객에게 보낼 웰컴 이메일 시퀀스 5개를 만들어주세요.

비즈니스: [설명]
목표: [첫 구매 유도 / 관계 구축 / 재방문 유도]

각 이메일에 발송 타이밍, 제목(A/B 안), 본문 초안을 포함해주세요.
```

### 5. 가격 전략 수립

```
당신은 가격 전략 컨설턴트입니다.

아래 정보를 바탕으로 가격 전략을 제안해주세요:

- 제품/서비스: [설명]
- 원가: [금액]
- 경쟁사 가격: [범위]
- 타겟 고객: [설명]
- 목표 마진: [%]

가격 설정 근거, 번들/패키지 옵션, 할인 전략을 포함해주세요.
```

